# OSX Development Setup - Instalation bash
# Inspired by jessfraz's Mac OS X Dev Setup - https://github.com/jessfraz/mac-dev-setup



clear


echo '----------------------------------------------------------------------------------------'
echo 'OSX DEVELOPMENT SETUP - INSTALATION BASH \n\n'
echo '----------------------------------------------------------------------------------------'


echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING XCODE'
echo '----------------------------------------------------------------------------------------'

xcode-select --install

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING HOMEBREW'
echo '----------------------------------------------------------------------------------------'

ruby -e "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/master/install)"

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING BASH COMPLETIONS'
echo '----------------------------------------------------------------------------------------'

brew install bash-completion

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING GIT'
echo '----------------------------------------------------------------------------------------'

brew install git

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING RVM + RAILS'
echo '----------------------------------------------------------------------------------------'

curl -sSL https://get.rvm.io | bash -s stable --rails

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING RUBY 2.2.2'
echo '----------------------------------------------------------------------------------------'

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING RUBY 2.1.1'
echo '----------------------------------------------------------------------------------------'

rvm install 2.2.2

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING RUBY 2.0.0'
echo '----------------------------------------------------------------------------------------'

rvm install 2.0.0

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING RUBY 1.9.3'
echo '----------------------------------------------------------------------------------------'

rvm install 1.9.0

echo '----------------------------------------------------------------------------------------'
echo 'SETTING DEFAULT RUBY'
echo '----------------------------------------------------------------------------------------'
/bin/bash --login
rvm --default use 2.1.1

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING BUNDLER IN THE DEFAULT RUBY VERSION'
echo '----------------------------------------------------------------------------------------'

gem install bundler

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING MIDDLEMAN'
echo '----------------------------------------------------------------------------------------'

gem install middleman

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING & UPDATING HEROKU'
echo '----------------------------------------------------------------------------------------'

brew install heroku-toolbelt
heroku update

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING VIRTUALBOX'
echo '----------------------------------------------------------------------------------------'

brew tap phinze/homebrew-cask
brew install brew-cask
brew cask install virtualbox

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING DOCKER'
echo '----------------------------------------------------------------------------------------'

brew update
brew install docker
brew install boot2docker


echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING MONGO'
echo '----------------------------------------------------------------------------------------'


brew install mongo

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING NVM'
echo '----------------------------------------------------------------------------------------'


curl -o- https://raw.githubusercontent.com/creationix/nvm/v0.33.1/install.sh | bash
[ -s "$NVM_DIR/nvm.sh" ] && \. "$NVM_DIR/nvm.sh"  # This loads nvm

echo '----------------------------------------------------------------------------------------'
echo 'INSTALLING STABLE NODE VERSION'
echo '----------------------------------------------------------------------------------------'

nvm install stable

echo '----------------------------------------------------------------------------------------'
echo 'SETTING DEFAULT NODE VERSION'
echo '----------------------------------------------------------------------------------------'

nvm alias default stable

